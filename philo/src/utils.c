#include "philo.h"

void	ft_putchar(const char c)
{
	write(1, &c, 1);
}

void	ft_putint(int i)
{
	if (i <= -1)
	{
		ft_putchar('-');
		if (i == -2147483648)
			write(1, "2147483648", 10);
		else
			ft_putint(i * -1);
	}
	else
	{
		if (i / 10)
			ft_putint(i / 10);
		ft_putchar(i % 10 + '0');
	}
}

int ft_strlen(const char *str)
{
	int p;

	p = 0;
	while (str[p])
		p++;
	return (p);
}

int	not_num(const char *str)
{
	int p;

	p = 0;
	while (str[p])
	{
		if (str[p] < '0' || str[p] > '9')
			return (1);
		p++;
	}
	return (0);
}

int	ft_atoi(const char *str)
{
	const char	sgn = 1 - (*str == '-') * 2;
	int			ret;

	ret = 0;
	str += (*str == '-' || *str == '+');
	while (*str == '0')
		str++;
	while ((*str >= '0' && *str <= '9' && ret <= 214748363)
		|| (*str >= '0' && *str <= '7' && ret == 214748364))
	{
		ret *= 10;
		ret += *str - '0';
		str++;
	}
	if (*str >= '0' && *str <= '9')
	{
		if (sgn == 1)
			return (2147483647);
		return (-2147483648);
	}
	return (ret * sgn);
}
